                <h3>Scratching</h3>
                <p>Cats need to scratch! When a cat scratches, the old outer nail sheath is pulled off and the sharp,
                    smooth claws underneath are exposed.
                    Cutting your cat’s nails every two to three weeks will keep them relatively blunt and less likely to
                    harm the arms of both humans and furniture.
                    Provide your cat with a sturdy scratching post, at least three feet high.
                    The post should also be stable enough that it won't wobble during use, and should be covered with
                    rough material such as sisal, burlap or tree bark. Many cats also like scratching pads.</p>
